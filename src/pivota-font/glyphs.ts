/**
 * Pivota Sans — glyph data (TypeScript)
 *
 * Pure data + metrics. No React, no DOM. Importable in any TS context
 * (browser, Node, RSC, edge). Path data is portable to FontForge /
 * Glyphs / Fontmake for OTF compilation later.
 *
 * Construction grid (em = 1000 units):
 *   cap top      y = 100   cap height 800
 *   x-height top y = 400   x-height   500
 *   baseline     y = 900
 *   descender    y = 1100
 *   ascender     y = 100  (same as cap)
 *
 * Each glyph is a stroke skeleton:
 *   - `paths`  centerline SVG path strings, stroked at runtime
 *   - `dots`   filled circles (i-dot, period, etc.)
 *   - `width`  advance width in em units
 *
 * Both weights (regular 90, bold 160) and italic (skewX -12°) derive
 * from the same data — no per-weight redraws.
 */

export interface PivotaFontMetrics {
  em: number;
  capTop: number;
  xHeight: number;
  baseline: number;
  descender: number;
  ascender: number;
  strokes: { regular: number; bold: number };
  italicAngle: number;
  letterSpacing: number;
  spaceWidth: number;
}

export interface PivotaGlyph {
  width: number;
  paths: string[];
  dots: Array<[number, number, number]>; // [cx, cy, r]
}

